#include <bits/stdc++.h>
using namespace std;

int main()
{
    int N;
    cin >> N;
    for (int i = 0; i < N; i++)
    {
        cout << i << endl;
    }
    return 0;
}

/**
 * 1.time complexity is O(N)
 * 2.space complexity is O(1)
 * 
 * above the code the time complexity depend on the N variable and how many steps need to complete code.
 */